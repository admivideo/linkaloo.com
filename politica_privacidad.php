<h1>Política de privacidad</h1>
<p>En linkaloo respetamos tu privacidad y nos comprometemos a proteger los datos personales que nos facilites. Esta política describe qué información recopilamos y cómo la usamos.</p>

<h2>Información que recopilamos</h2>
<p>Recopilamos los datos que introduces al registrarte y al usar el servicio, como tu nombre, dirección de correo electrónico y los enlaces que guardas.</p>

<h2>Uso de la información</h2>
<p>Utilizamos tus datos para proporcionarte acceso a linkaloo, mantener tu cuenta, enviar comunicaciones relacionadas con el servicio y mejorar nuestras funciones.</p>

<h2>Compartición de datos</h2>
<p>No vendemos tus datos personales. Solo los compartimos con proveedores que nos ayudan a prestar el servicio y cuando la ley lo exige.</p>

<h2>Conservación y seguridad</h2>
<p>Mantenemos tus datos solo el tiempo necesario para ofrecer el servicio. Aplicamos medidas técnicas y organizativas para protegerlos contra el acceso no autorizado.</p>

<h2>Derechos del usuario</h2>
<p>Puedes acceder, corregir o eliminar tus datos personales. Para ejercer estos derechos, escríbenos a support@linkaloo.com.</p>

<h2>Cambios a esta política</h2>
<p>Podemos actualizar esta política ocasionalmente. Publicaremos la versión revisada indicando la fecha de última actualización.</p>

<h2>Contacto</h2>
<p>Si tienes preguntas sobre esta política, contacta con nosotros en support@linkaloo.com.</p>
